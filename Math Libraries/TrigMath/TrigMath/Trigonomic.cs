    public class Trigonomic
    {
        //Tangent method that returns a double
        public static double Tan(double a)
        {
            return Math.Tan(a);
        }

        //Sine method that returns a double
        public static double Sin(double a)
        {
            return Math.Sin(a);
        }

        //Cosine method that returns a double
        public static double Cos(double a)
        {
            return Math.Cos(a);
        }
    }
